import java.util.*;
public class java22_arrayForEachLoop {
    public static void main(String[] args) {
        
    Scanner sc=new Scanner(System.in);
    int Marks[]={1,2,3,4,5};
    for(int m:Marks){
       System.out.print(m+" ");
    }

    sc.close();
    
    } 
}